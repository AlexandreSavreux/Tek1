int main(int argc, char **argv )
{
	int i = 0;
	int j = 1;

	if (argc > i) {
		while(i < argv[i]) {
			my_putstr(argv[argc - j]);
			my_putchar('\n');
			j++;
			i++;
		}
	}
	return (0);
}
